package data;

public class Coordinates {
    private float x;
    private double y; //Максимальное значение поля: 777

    public void setX(float x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public float getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

}